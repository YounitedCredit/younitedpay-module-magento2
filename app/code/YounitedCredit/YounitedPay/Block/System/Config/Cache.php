namespace YounitedCredit\YounitedPay\Block\System\Config;

use Magento\Backend\Block\Template\Context;
use Magento\Config\Block\System\Config\Form\Field;
use Magento\Framework\Data\Form\Element\AbstractElement;

/**
 * Class Cache
 *
 * @package YounitedCredit\YounitedPay\Block\System\Config
 */
class Cache extends Field
{

    /**
     * @var string
     */
    protected $_template = 'YounitedCredit_YounitedPay::system/config/cache.phtml';

    /**
     * Cache constructor.
     *
     * @param Context $context
     * @param array $data
     */
    public function __construct(
        Context $context,
        array $data = []
    ) {
        parent::__construct($context, $data);
    }

    /**
     * Remove scope label
     *
     * @param AbstractElement $element
     *
     * @return string
     */
    public function render(AbstractElement $element)
    {
        $element->unsScope()->unsCanUseWebsiteValue()->unsCanUseDefaultValue();

        return parent::render($element);
    }

    /**
     * Return element html
     *
     * @param AbstractElement $element
     *
     * @return string
     */
    protected function _getElementHtml(AbstractElement $element)
    {
        return $this->_toHtml();
    }

    /**
     * Return url for clean cache button
     *
     * @return string
     */
    public function getAjaxUrl()
    {
        return $this->getUrl('younitedcredit/cache/clean');
    }

    /**
     * Get HTML
     *
     * @return mixed
     * @throws \Magento\Framework\Exception\LocalizedException
     */
    public function getButtonHtml()
    {
        $button = $this->getLayout()->createBlock(
            'Magento\Backend\Block\Widget\Button'
        )->setData(
            [
                'id' => 'younited_flush_cache',
                'label' => __('Flush cache'),
            ]
        );

        return $button->toHtml();
    }
}
